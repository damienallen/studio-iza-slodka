                <div class="col">
                    <p>
                        Izabela Slodka studied in Cracow, Poland and a Delft University of Technology, where she
                        received her master's degree in 2016.
                        Her project “Collage and Continuity” was nominated for Young Talent Architecture
                        Award – part of the European Union Prize
                        for Contemporary Architecture – Mies van der Rohe Award, organised by the Fundació Mies van der
                        Rohe.
                        After winning Europan 15 competition in 2019 with her proposal Makers’ Maze, she founded her own
                        studio.
                    </p>
                    <p>
                        Both “Collage and Continuity” and “Makers’ Maze” illustrate Iza’s fascination with
                        post-industrial sites.
                        They are part of ongoing research focusing on unique spatial and material qualities of these,
                        often forgotten, parts of the cities.
                        She is looking for existing conditions that attract makers, designers, creating vibrant and
                        creative atmosphere.
                    </p>
                </div>

                <div class="col">
                    <p>
                        Informal and organic character of these sites encourage freedom and unexpected to happen,
                        enhancing the idea of
                        untamed and exciting urban environment. This city ecology is built over time and consist of
                        different elements and layers,
                        allowing various participants and functions to coexist.
                    </p>
                    <p>
                        Recurring interest is a collage of elements and their relation to each other:
                        in terms of building and outdoor space, structure and cladding, materiality and color.
                        Architecture is treated as an envelope of a public
                        space rather than an independent structures, unless emphasized otherwise. Urban space, landscape
                        and buildings are always designed together
                        as an integral parts of the same concept.
                    </p>
                </div>

