                            with the economic, social and political
                            realities of the contemporary city by
                            offering a flexible method rather than a
                            rigid design. The goal is to create new
                            conditions for creativity, circularity,
                            interaction and sharing while
                            preserving the existing character.
                        </p>
                    </div>
                    <div class="col">
                        <p class="hide-sm">
                            The charm of the Vierhavensblok is
                            its informal character where one finds
                            the unexpected around each corner
                            and where chance encounters breed
                            new collaborations. Inspired by these
                            qualities I proposed a series of interlocking
                            building clusters organized around
                            a network of passages, streets and
                            squares, creating a unified, resilient,
                            and intriguing urban scheme. The ambition
                            was to build on the present sense of
                            community and self-initiative by leaving
                            enough room for organic growth and
                            adaptation. 
